<h2>Operadores relacionais</h2>
<hr>
<p>
    São operadores utilizados para verificar
    a relação (maior, menor, diferenta) entre
    valores.
</p>

<strong>Operadores</strong>
<ol>
    <li>> | Maior</li>
    <li>< | Menor</li>
    <li>>= | Maior igual</li>
    <li><= | Menor igual</li>
    <li>== | Igual</li>
    <li>!= | Diferenta</li>
    <li>=== | Identico - compara po valor</li>
    <li>!== | Não identico - compara valor e tipo</li>
</ol>
<strong>Exemplos</strong>

<pre>
    &lt;?php
    $resposta = 10 == 10;
    var_dump($resposta);
    ?&gt;
</pre>

    <?php
    $resposta = 10 == 10;
    var_dump($resposta);
    ?>
    
    <?php
    $resposta = 10 == 10;
    var_dump($resposta);
    ?>